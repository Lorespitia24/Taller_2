package dao;

public class Calculadora {

    public double suma(double valor, double valor2){
		return valor + valor2;
	}
		
	public double resta(double valor, double valor2){
        return valor + valor2;
    }
    
    public double multiplicacion(double valor, double valor2){
        return valor + valor2;
    }
    
    public double division(double valor, double valor2){
        return valor / valor2;
    }
    
}
